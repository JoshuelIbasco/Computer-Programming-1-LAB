#include <iostream>
using namespace std;

int main()
{
	int Number = 10;
	char Letter = 'a';
	bool Result = true;
	float Float = 3.14;
	
	cout << "Integer: " << Number << endl;
	cout << "Character: " << Letter << endl;
	cout << "Boolean: " << (Result ? "True":"False") << endl;
	cout << "Float: " << Float << endl;
	return 0;
}
